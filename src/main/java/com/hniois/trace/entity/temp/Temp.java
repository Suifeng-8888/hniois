package com.hniois.trace.entity.temp;

import com.hniois.common.page.Page;

/**
 * 每个二维码图片对象
 */
public class Temp extends Page {
    private String img;//二维码图片
    private String name;//公司名称
    private String tel;//公司电话
    private String random;//防伪码
    private String mark;//顺序码
    private String batch;//产品批次
    private String rule_code;//国际标段
    private String begin_code;//起始码
    private String end_code;//结束码
    private String address;//公司地址

    public String getRule_code() {
        return rule_code;
    }
    public void setRule_code(String rule_code) {
        this.rule_code = rule_code;
    }
    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getRandom() {
        return random;
    }

    public void setRandom(String random) {
        this.random = random;
    }

    public String getMark() {
        return mark;
    }

    public void setMark(String mark) {
        this.mark = mark;
    }

    public String getBegin_code() {
        return begin_code;
    }

    public void setBegin_code(String begin_code) {
        this.begin_code = begin_code;
    }

    public String getEnd_code() {
        return end_code;
    }

    public void setEnd_code(String end_code) {
        this.end_code = end_code;
    }

    public String getBatch() {
        return batch;
    }

    public void setBatch(String batch) {
        this.batch = batch;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
}
